        <section class="about">
            <h2>About</h2>

            <p>
                Maxwell the Cat, also known as Spinning Cat is a black-and-white tuxedo cat who
                achieved virality online in October and November 2022 after her model was first
                added to Garry's Mod and later used as source material for videos in which it spins
                around as various songs play, similar to the Horizontally Spinning Rat memes.
                The cat is also known as Dingus the Cat and Big Boobs, and its real name is Jess.*
            </p>
        </section>
        <section class="origin">
            <h2>Origin</h2>

            <p>
                On November 20th, 2017, Imgur[11] user Voidhawk42 posted an image of a black-and-white cat
                sitting on a piece of paper reading "please do not bend". Starting in 2020, the image
                was actively circulated through reposts on Facebook and Twitter.
                On October 12th, Steam[4] user шизео кодзима added a 3D model of the cat,
                usable in Garry's Mod and other Source games, to Steam Workshop, where it
                was viewed over 20,000 times and favorited over 500 times in two months.
            </p>
        </section>
        <section class="identity">
            <h2>Identity</h2>

            <p>
                The cat, whose name is Jess, belongs to Imgur user Voidhawk42.
                Online, the cat has been misidentified as Dingus the Cat, the black-and-white
                cat who can be found on @BigOldBuns TikTok account and @big_old_buns Instagram
                account, and as Uni the Cat, a similarly-looking cat, mostly due to a viral His Name Is X,
                Look Him Up meme posted in November 2021 by Twitter user @calicokritter.
            </p>

        </section>

        <section class="spread">
            <h2>Spread</h2>
            <p>
                On October 21st, 2022, Steam[5] user ScottRim modified the model into a carryable item, 
                with the model garnering over 119,000 unique visitors and being favorited over 6,700 times 
                in one month (shown below). On the Steam Workshop page, the cat was referred to as "Maxwell 
                the Carryable Cat."On October 25th, 2022, YouTuber[6] user Awesome Te4m posted a humorous 
                Half-Life 2 video titled, "Half-Life 2, but a have a cat," in which he used the model. 
                The video (shown below) received over 639,000 views in one month, On November 4th, 2022, 
                Twitter[7] user @blastycat posted a GIF of the cat model spinning, with the post gaining 
                over 34,100 retweets and 161,300 likes in one month (shown below). On November 5th, 
                Twitter[8] user @hacer_kun posted an edited version of the GIF, captioned, "You have 
                acquired CAT!" The post gained over 1.4 million views, 35,800 retweets and 171,200 likes 
                (shown below). On November 6th, 2022, Tumblr[9] user escuerzoresucitado reposted the GIF. 
                On November 8th, Tumblr[10] user funke replied with the song Around the World by Daft Punk, 
                with the exchange gaining over 69,000 reblogs and 83,500 likes in three weeks.
                Following the posts, the GIF of the cat model spinning achieved viral popularity online 
                as it was combined with various songs. Among them, the melody "Stockmarket" by Weebl 
                gained particular popularity, becoming known as "Maxwell's Theme."[15]
